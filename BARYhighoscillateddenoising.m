% high oscillated denoising
% See Experiment 3

clear all, close all
N = 500; L = N; LL = 100;
ff = -1:0.01:1; [f,w,v] = jacpts(N+1,-.5,-.5);
GG =  airy(40*ff); G= airy(40*f);
[YY,NOISE] = noisegen(GG,10); [Y,NOISE] = noisegen(G,12);
lambda = [0 10^(-1) 10^(-2) 10^(-5)];
for l = 0:1:L
    mu(l+1) = 1/Filter(L,l);
end
mu = mu';
for l = 0:L
    for j = 0:N
        A(j+1,l+1) = cos(l*acos(f(j+1)))/sqrt(pi/2);
    end
end
A(:,1) = A(:,1)/sqrt(2);

