a1 = zeros(L+1,1); a2 = zeros(L+1,1); a3 = zeros(L+1,1);
Ax = A'*(w'.*Y);
for k = 2:4
for i = 0:L
    if 2*Ax(i+1)>lambda(k)*mu(i+1)
        a(i+1,k-1) = -lambda(k)*mu(i+1)/2;
    elseif 2*Ax(i+1)<-lambda(k)*mu(i+1)
        a(i+1,k-1) = lambda(k)*mu(i+1)/2;
    else
        a(i+1,k-1) = -Ax(i+1);
    end
end
end
